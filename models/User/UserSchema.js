const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  refreshToken: { type: String },

});
const UserModel = mongoose.model("UserModel", UserSchema);
module.exports = UserModel;
